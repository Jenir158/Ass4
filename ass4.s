.syntax unified
.equ GRADE_A_THRESHOLD, 90
.equ GRADE_B_THRESHOLD, 75
.equ GRADE_C_THRESHOLD, 50

ldr R5, =75

cmp R5, #GRADE_A_THRESHOLD
bge A_GRADE_CHECK

cmp R5, #GRADE_B_THRESHOLD
bge B_GRADE_CHECK
b NOT_A_OR_B_GRADE

A_GRADE_CHECK:
b DONE

B_GRADE_CHECK:
cmp R5, #GRADE_C_THRESHOLD
bge C_GRADE_CHECK
b NOT_C_GRADE

C_GRADE_CHECK:
b DONE

NOT_C_GRADE:
b DONE

NOT_A_OR_B_GRADE:
b DONE

DONE:
